// Print numbers from 1 to 100 using a while loop.
public class task01 {
    public static void main(String[] args) {
        System.out.println("Numbers from 1 to 100:");

        // Use a while loop to iterate through numbers from 1 to 100
        int number = 1;
        while (number <= 100) {
            System.out.println(number); // Print the current number
            number++; // Increment the number to move to the next one
        }
    }
}
