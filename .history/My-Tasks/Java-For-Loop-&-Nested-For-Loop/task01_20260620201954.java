// Task 01 – Pattern Printing
public class task01 {
    public static void main(String[] args) {

        // Output: Pattern Printing
        for (int i = 1; i <= 5; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
