// Task 03 – Number Pattern

public class task03 {
    public static void main(String[] args) {

        // Output: Number Pattern
        for (int i = 1; i <= 5; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print(j);
            }
            System.out.println();
        }
    }
}
