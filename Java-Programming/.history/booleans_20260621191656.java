public class boolean {
    public static void main(String[] args) {
        boolean isJavaFun = true;
        boolean isFishTasty = false;

        System.out.println("Is Java fun? " + isJavaFun);
        System.out.println("Is fish tasty? " + isFishTasty);

        // Using boolean in conditions
        if (isJavaFun) {
            System.out.println("I love programming in Java!");
        } else {
            System.out.println("I don't like programming in Java.");
        }

        if (isFishTasty) {
            System.out.println("I enjoy eating fish.");
        } else {
            System.out.println("I don't enjoy eating fish.");
        }
    }
}
